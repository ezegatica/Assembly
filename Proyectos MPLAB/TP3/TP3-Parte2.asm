#INCLUDE <P16F84A.INC>
SETUP
    BSF STATUS, RP0
    MOVLW 0x00
    MOVWF TRISA
    MOVLW 0x02
    MOVWF TRISB
	BCF OPTION_REG, NOT_RBPU
    BCF STATUS, RP0

LOOP
    MOVLW 0X00
	MOVWF PORTA
	BTFSS PORTB, RB1
    GOTO PRENDER
	GOTO LOOP

PRENDER
	MOVLW 0x07
	MOVWF PORTA
	BTFSS PORTB, RB1
	GOTO PRENDER
	GOTO LOOP

END