#INCLUDE <P16F84A.INC>
SETUP
    BSF STATUS, RP0
    MOVLW 0x02
    MOVWF TRISB
    BCF OPTION_REG, NOT_RBPU
    BCF STATUS, RP0

MAIN
    CBLOCK 0x20
    CONTADOR
    ENDC
    GOTO LOOP

LOOP
    BTFSC PORTB, RB1
    GOTO LOOP
    GOTO INCREMENTAR

INCREMENTAR
    INCF CONTADOR
    ESPERAR
        BTFSC PORTB, RB1
        GOTO LOOP
        GOTO ESPERAR

END